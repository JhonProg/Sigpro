	private int idpedido;
	private int idproducto;
	private int cantidad;
	
	public int getIdpedidodetalle() {
		return idpedidodetalle;
	}
	public void setIdpedidodetalle(int idpedidodetalle) {
		this.idpedidodetalle = idpedidodetalle;
	}
